# e) Crie um programa que solicita um número e, se for par, exibe "O
# número é par"; caso contrário, exibe "O número é ímpar".

numero = int(input("Digite um número: "))

if numero % 2 == 0:
    print("O número é par")
else:
    print("O número é ímpar")
    
    
